W1 = rand(3, size(X,1));%aqui escolho qual dimensão os pontos serão projetados
W2 = rand(size(X,1), size(W1,1));
B1 = rand(size(W1,1),1);
B2 = rand(size(X,1),1);

%Gradiente estocástico
for ee = 1:epoch
    I = randperm(size(X,2));
    for i = 1:length(I)
        x = X(:,i);
        z = tanh(W1*x + B1);
        y = tanh(W2*z + B2);
        %backpropagation
        z_p = tanh_prime(z);
        y_p = tanh_prime(y);
        e = y - x;
        delta_1 = e.*y_p;
        dE_dW2 = delta_1*z';
        dE_dB2 = delta_1;
        delta_2 = (W2'*(delta_1)).*z_p;
        dE_dW1 = delta_2*x';
        dE_dB1 = delta_2;
        W1 = W1 - alfa*dE_dW1;
        B1 = B1 - alfa*dE_dB1;
        W2 = W2 - alfa*dE_dW2;
        B2 = B2 - alfa*dE_dB2;
    end
    J(ee) = norm(e);
    plot(J)
    drawnow
end

